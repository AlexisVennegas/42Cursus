#include <unistd.h>

void	ft_print(char c)
{
	write(1, &c, 1);
}

int	main(void)
{
	int	m[4][4] = {{4, 3, 2, 1}, {1, 2, 2, 2}, {4, 3, 2, 1}, {1, 2, 2, 2}};
	int	tb[4][4];
	int	x;

	x = 0;
	if (m[2][0] == 4 && m[0][3] == 1)
	{
		ft_print(tb[0][0] = 1 + '0');
		ft_print(tb[0][1] = 2 + '0');
		ft_print(tb[0][2] = 3 + '0');
		ft_print(tb[0][3] = 4 + '0');
		ft_print('\n');
	}
	if (m[2][1] == 3 && m[3][1] == 2)
		ft_print(tb[1][0] = 2 + '0');
	ft_print(tb[1][1] = 3 + '0');
	ft_print(tb[1][2] = 4 + '0');
	ft_print(tb[1][3] = 1 + '0');
	ft_print('\n');
	tb[3][1] = 1;
	if (m[2][2] == 2 && m[3][2] == 2)
		ft_print(tb[2][0] = 3 + '0');
	ft_print(tb[2][1] = 4 + '0');
	ft_print(tb[2][2] = 1 + '0');
	ft_print(tb[2][3] = 2 + '0');
	ft_print('\n');
	if (m[2][3] == 1 && m[3][3] == 2)
		ft_print(tb[3][0] = 4 + '0');
	ft_print(tb[3][1] = 1 + '0');
	ft_print(tb[3][2] = 2 + '0');
	ft_print(tb[3][3] = 3 + '0');
}
